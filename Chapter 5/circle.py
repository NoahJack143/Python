#this circle module has functions that perform
#calculations related to circles.
import math

def area(radius):
    #area accepts an integer for radius
    #it calculates and returns the area of a circle
    
    return math.pi * radius**2

def circumference(radius):
    #circumference accepts an argument for radius
    #it calculates and returns the redius of a circle
    
    return 2 * math.pi * radius
